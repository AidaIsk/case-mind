_PHASE_COLOR = {
    "Placement":   "#d32f2f",
    "Layering":    "#f57c00",
    "Integration": "#388e3c",
}

_PHASE_ICON = {
    "Placement":   "🔴",
    "Layering":    "🟠",
    "Integration": "🟢",
}

_OUTCOME_ICON = {
    "EDD":     "🔵",
    "Reject":  "🔴",
    "monitor": "🟡",
    "SAR":     "🟠",
}


def _phase_badge(phase: str) -> str:
    icon = _PHASE_ICON.get(phase, "⚪")
    return f"{icon} {phase}"


def _outcome_icon(outcome_text: str) -> str:
    """Возвращает иконку для likely_outcome по первому совпадению."""
    lo = outcome_text.lower()
    if "reject" in lo:  return "🔴"
    if "edd" in lo:     return "🔵"
    if "sar" in lo:     return "🟠"
    return "🟡"


