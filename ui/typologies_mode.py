# ---------------------------------------------------------------------------
# UI helpers
# ---------------------------------------------------------------------------

_PHASE_COLOR = {
    "Placement":   "#d32f2f",
    "Layering":    "#f57c00",
    "Integration": "#388e3c",
}

_PHASE_ICON = {
    "Placement":   "🔴",
    "Layering":    "🟠",
    "Integration": "🟢",
}


def _phase_badge(phase: str) -> str:
    icon = _PHASE_ICON.get(phase, "⚪")
    return f"{icon} {phase}"


# ---------------------------------------------------------------------------
# Render functions
# ---------------------------------------------------------------------------

def _render_three_phases() -> None:
    """Краткое объяснение трёх фаз отмывания."""
    st.markdown("### Три фазы отмывания денег")
    st.caption(
        "Понимание фаз помогает определить, на каком этапе находится подозрительная активность "
        "и какие признаки искать."
    )
    c1, c2, c3 = st.columns(3)
    for col, (phase, description) in zip([c1, c2, c3], PHASES.items()):
        with col:
            icon = _PHASE_ICON[phase]
            st.markdown(f"**{icon} {phase}**")
            st.caption(description)


def _render_typology_card(t: dict) -> None:
    """Рендерит одну typology card в expander."""
    phase_badge = _phase_badge(t["phase"])
    with st.expander(f"**{t['id']}  {t['name']}** — {phase_badge}", expanded=False):
        st.caption(t["short_def"])
        st.divider()

        col_l, col_r = st.columns(2)

        with col_l:
            st.markdown("**Цель преступника**")
            st.caption(t["criminal_goal"])

            st.markdown("**Основные признаки**")
            for flag in t["red_flags"]:
                st.caption(f"• {flag}")

            st.markdown("**Типичная ошибка аналитика**")
            st.warning(t["analyst_mistake"], icon="⚠️")

        with col_r:
            st.markdown("**С чем можно спутать**")
            st.caption(t["confusion_with"])

            st.markdown("**Что проверить дальше**")
            for step in t["check_next"]:
                st.caption(f"→ {step}")


def _render_typologies_list(filter_phase: str) -> None:
    """Рендерит все карточки с учётом фильтра по фазе."""
    filtered = (
        TYPOLOGIES if filter_phase == "Все"
        else [t for t in TYPOLOGIES if t["phase"] == filter_phase]
    )

    if not filtered:
        st.info("Нет типологий для выбранной фазы.")
        return

    st.caption(f"Показано карточек: {len(filtered)}")
    for t in filtered:
        _render_typology_card(t)


# ---------------------------------------------------------------------------
# Main render function (called from app.py)
# ---------------------------------------------------------------------------

def render_typologies_tab() -> None:
    st.header("Типологии")
    st.caption(
        "Обучающий справочник по основным схемам отмывания денег. "
        "Для каждой типологии — признаки, цель, типичные ошибки и что проверить дальше."
    )

    _render_three_phases()

    st.divider()

    # ── Фильтр по фазе ───────────────────────────────────────────────────
    st.markdown("### Типологии")
    phase_options = ["Все", "Placement", "Layering", "Integration"]
    selected_phase = st.radio(
        "Фаза:", phase_options, horizontal=True, key="typology_phase_filter"
    )

    st.write("")
    _render_typologies_list(selected_phase)
